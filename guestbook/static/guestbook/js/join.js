// https://en.wikipedia.org/w/index.php?title=Fisher%E2%80%93Yates_shuffle#JavaScript_Implementation
function shuffleArray(array) {
    for (let i = array.length - 1; i >= 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

// Prepare shuffled lists for user.
shuffleArray(NAMES);
shuffleArray(ADJECTIVES);

window.onload = function () {

    let shuffleNameButton = document.getElementById("shuffle-name-button");
    let nameInput = document.getElementById("name-input");
    let index = 0;

    // Set input to first pair.
    nameInput.value = generateRandomName(index, index)

    shuffleNameButton.addEventListener("click", function () {
        ++index;
        nameInput.value = generateRandomName(index, index);
    });

    function generateRandomName(adjIndex, nameIndex) {
        // Avoid index out of bounds.
        adjIndex %= ADJECTIVES.length;
        nameIndex %= NAMES.length;

        let fullName = capitalize(ADJECTIVES[adjIndex]) + " " + capitalize(NAMES[nameIndex]);
        // https://github.com/moby/moby/blob/b5d5fef7aa68f160b817edef186c9b18ab996f4f/pkg/namesgenerator/names-generator.go#L852
        if (fullName === "Boring Wozniak") /* Steve Wozniak is not boring */ {
            fullName = generateRandomName(adjIndex, nameIndex + 1);
        }

        return fullName;
    }

    function capitalize(s) {
        return String(s[0]).toUpperCase() + String(s).slice(1);
    }

};
